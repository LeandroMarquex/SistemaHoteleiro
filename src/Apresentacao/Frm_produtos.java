    private void btn_apagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_apagarActionPerformed
        // TODO add your handling code here:
        if (!txt_id_produtos.getText().equals("")) {
            int confirmacao = JOptionPane.showConfirmDialog(rootPane, "Deseja apagar o PRODUTO");
            if (confirmacao == 0) {
                FProdutos func = new FProdutos();
                VProdutos dts = new VProdutos();
                dts.setIdProduto(Integer.parseInt(txt_id_produtos.getText()));
                func.deletar(dts);
                mostrar("");
                desativar();
            }
        }
    }//GEN-LAST:event_btn_apagarActionPerformed

