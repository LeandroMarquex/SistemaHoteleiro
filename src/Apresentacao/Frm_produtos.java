    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
        // TODO add your handling code here:
        if (txt_nome.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Insira o NOME do produto");
            txt_nome.requestFocus();
            return;
        }
        if (txt_descricao.getText()
            .length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Insira a DESCRIÇÃO do produto");
            txt_descricao.requestFocus();
            return;
        }

        

        if (txt_valorDiaria.getText()
            .length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Insira o VALOR da DIÁRIO do produto");
            txt_valorDiaria.requestFocus();
            return;
        }
        VProdutos dts = new VProdutos();
        FProdutos func = new FProdutos();

        dts.setNomeProduto(txt_nome.getText());
        dts.setValorProduto(Double.parseDouble(txt_valorDiaria.getText()));
        dts.setDescricaoProduto(txt_descricao.getText());
        

        int selecionaMedida = cb_medida.getSelectedIndex();
        dts.setUnidade_medida((String) cb_medida.getItemAt(selecionaMedida));



        if (acao.equals("salvar")) {
            if (func.inserir(dts)) {
                JOptionPane.showMessageDialog(rootPane, "O PRODUTO foi inserido com SUCESSO");
                mostrar("");
                desativar();
            }

        } else if (acao.equals("editar")) {
            dts.setIdProduto(Integer.parseInt(txt_id_produtos.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "O PRODUTO foi EDITADO com SUCESSO");
                mostrar("");
                desativar();
            }
        }
    }//GEN-LAST:event_btn_salvarActionPerformed

